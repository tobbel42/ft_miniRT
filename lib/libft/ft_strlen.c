/* 																			  */
/* Input 1: pointer to string s												  */
/* Return: size_t with length of string s									  */
/* 																			  */
/* String length returns the length of string s.							  */
/* 																			  */
/* ************************************************************************** */

#include "libft.h"

size_t	ft_strlen(const char *s)
{
	if (*s != '\0')
		return (1 + ft_strlen(++s));
	else
		return (0);
}
